export const calculateSizes = (isSmall, isMobile, isTablet) => {
  return {
    deskScale: isSmall ? 2.8 : isMobile ? 2.7 : isTablet ? 2 : 1.9,
    // deskPosition: isMobile ? [0.5, -4.5, 0] : [0.25, -5.5, 0],  0, -4.7, 10
    deskPosition: isSmall
      ? [0, -2.4, 0]
      : isMobile
      ? [0, -2.9, 0]
      : isTablet
      ? [0, -2.7, 0]
      : [0, -2.5, 0],
    violatorPosition: isSmall
      ? [4, -5, 0]
      : isMobile
      ? [5.1, -5, 0]
      : isTablet
      ? [9, -5, 0]
      : [9, -5.5, 0],
    reactLogoPosition: isSmall
      ? [3, 4, 0]
      : isMobile
      ? [5, 4, 0]
      : isTablet
      ? [5, 4, 0]
      : [12, 3, 0],
    nodePosition: isSmall
      ? [-5, 7, 0]
      : isMobile
      ? [-10, 10, 0]
      : isTablet
      ? [-12, 10, 0]
      : [-24, 10, 0],
    robotPosition: isSmall
      ? [-5, -10, -10]
      : isMobile
      ? [-6.9, -10, -10]
      : isTablet
      ? [-11, -7, -10]
      : [-13, -13, -10],
  };
};

